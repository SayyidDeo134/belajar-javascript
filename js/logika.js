// Operator logika
let a = true;
let b = false;
let c = true;

// Operator OR
// Jika salah satu bernilai true maka hasilnya adalah true
// Jika ingin bernilai false maka semua nilai variabel harus false
console.log(a || b);
// hasilnya adalah true

// Operator AND
// Jika salah satu bernilai false maka hasilnya adalah false
// Jika ingin bernilai true maka semua nilai variable harus true
console.log(a && b);
// hasilnya adalah false

