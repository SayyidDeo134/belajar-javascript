let mulai = 0;
let selesai = 10;

// Menggunakan while
while (mulai < selesai) {
    console.log(mulai);
    mulai++;
}
// Menggunakan do while
let start = 1;
let finish = 10;
do {
    console.log(start);
    start++;
} while (start < finish);

// Menggunakan for
for (let i = 0; i < 10; i++) {
    console.log(i);
}